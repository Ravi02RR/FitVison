import mongoose from "mongoose";
const schema = mongoose.Schema


const userSchema = new schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
}, { timestamps: true });
const UserModel = mongoose.model('User', userSchema);

export default UserModel;